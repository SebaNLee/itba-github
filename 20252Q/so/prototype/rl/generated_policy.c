// Clasifica celda: <=0 bloqueada, 1..4 libre-baja, 5..9 libre-alta
static inline int classify_cell_c(int v) {
    if (v <= 0) return 0;
    return (v >= 5) ? 2 : 1;
}

// Encode del estado local: vecinos en orden de acciones, base-3
static inline int encode_local_state_c(const int *board, int width, int height, int x, int y) {
    int idx = 0;
    for (int a = 0; a < 8; ++a) {
        int nx = x + DX[a], ny = y + DY[a];
        int cls = 0;
        if (nx >= 0 && nx < width && ny >= 0 && ny < height) {
            int v = board[ny * width + nx];
            cls = classify_cell_c(v);
        }
        idx = idx * 3 + cls; // base-3
    }
    return idx; // 0..6560
}

// Greedy seguro (elige mejor celda libre >0; si ninguna, devuelve 0)
static inline int fallback_greedy(const int *board, int width, int height, int x, int y) {
    int best_a = 0;
    int best_v = -10;
    for (int a = 0; a < 8; ++a) {
        int nx = x + DX[a], ny = y + DY[a];
        if (nx >= 0 && nx < width && ny >= 0 && ny < height) {
            int v = board[ny * width + nx];
            if (v > 0 && v > best_v) { best_v = v; best_a = a; }
        }
    }
    return best_a;
}

// Firma pedida por tu proyecto
int get_next_move(Tgame_state *game_state, int player_number) {
    const int width = game_state->width;
    const int height = game_state->height;
    const int x = game_state->players[player_number].x;
    const int y = game_state->players[player_number].y;
    const int *board = game_state->board;

    // Codificar estado local y consultar política
    int idx = encode_local_state_c(board, width, height, x, y);
    int action = POLICY[idx];

    // Validar acción propuesta (por si el estado apareció en training pero ahora la celda no está libre)
    if (action >= 0) {
        int nx = x + DX[action], ny = y + DY[action];
        if (nx >= 0 && nx < width && ny >= 0 && ny < height) {
            int v = board[ny * width + nx];
            if (v > 0) {
                return action; // válida
            }
        }
    }

    // Si no hay política para este estado o la acción ya no es válida -> greedy seguro
    return fallback_greedy(board, width, height, x, y);
}
